// Take a floating-point number as input, typecast it into an integer, and print both values.
#include<bits/stdc++.h>
using namespace std;
int main()
{
    float x;
    cout<<"Enter a floating point number :";
    cin>>x;
    cout<<"The value of floating point number :"<<x<<endl;
    cout<<"Corresponding integer value :"<<int(x); 
}